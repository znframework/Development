	|          																				  |
	******************************************************************************************/
	public function columnData()
	{
		if( empty($this->query) ) 
		{
			return false;
		}
		
		$columns = array();
		
		for ($i = 0, $c = $this->num_fields(); $i < $c; $i++)
		{
			$info = sybase_fetch_field($this->query, $i);
			
			$columns[$i]			= new stdClass();
			$columns[$i]->name		= $info->name;
			$columns[$i]->type		= $info->type;
			$columns[$i]->maxLength	= $info->max_length;
		}
		
		return $columns;
	}
	
